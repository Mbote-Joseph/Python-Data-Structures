# Doubly Linked List
# Link - Each link of a linked list can store a data called an element
# Next - Each link of a linked list contains a link to the next link called Next.
# Prev - Each link of a linked list contains a link to the previous link called Prev.
# Linked List - A linked list contains the connection link to the first link called First and to the last Link called Last.

# ============== Doubly Linked List features:
# - It contains a link element called first and last
# - Each link carries a data field(s) and a link field called next
# - Each link is linked with its next link using its next link.
#  - Each link is linked with it's previous link using its previous link
# - The last link carries a link as null to mark the end of the list

# ============= Basic Operations
# 1. Insertion - Adds an element at the beginning of a list
# 2. Insert Last - Adds an element at the end of a list
# 3. Insert After - Adds an element after an item of the list
# 4. Deletion - Deletes an element at the beginning of the list
# 5. Delete Last - Deletes an element from the end of the list
# 6. Delete - Deletes an element from the list using the key
# 7. Display forward - Display the complete list in a forward manner
# 8. Display Backwards - Display the complete list in a backward manner