# Types of Data Structures
# - primitive
    # Integer
    # Float
    # Character
    # String
    # Boolean
# - Non-primitive
    # Linear
        # Static
            # Array - A collection of elements stored at contiguous memory locations. It is a sequential arrangement of data elements paired with the index of data element.
            # Matrix - It is a 2D data structure in which the data element is referred by a pair of indices.
        # Dynamic
            # Linked List - Each data element contains a link to another element, along with the data present in it.
            # Stack -  It is a data structure, which follows a specific order of operation. LIFO (Last In First Out)
            # Queue - It is similar to stack, but order of operation is only FIFO (First In First Out)
    # Non-Linear
        # Binary Tree - It is a data structure, where each data element can be connected to maximum two other data elements and it starts with a root node.
        # Heap - It is a special case of Tree data Structure, where the data in the parent node is either strictly greater than/equal to the child nodes or strictly less than its child node.
        # Hash Table - It is a data structure, which is made of array associated with each other using hash function. It retrieves values using keys rather than index from a data element.
        # Graph - It is an arrangement of vertices and nodes, where some of the nodes are connected to each other through links.